nombre = input("¿Cómo te llamas? ")
edad = int(input("¿Cuántos años tienes? "))

print("\nHola", nombre)
print("Tienes", edad, "años")

# Operaciones con variables
suma = edad + 5
resta = edad - 2
multiplicacion = edad * 2
division = edad / 2

print("\nOperaciones con tu edad:")
print("Edad + 5 =", suma)
print("Edad - 2 =", resta)
print("Edad * 2 =", multiplicacion)
print("Edad / 2 =", division)
